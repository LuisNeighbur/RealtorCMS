        <p>
			&nbsp;If you are relocating to Houston, Texas, you must learn something about it first. &nbsp;Houston ranked No. 4 on Forbes’ list of “The U.S. Regions to Watch in 2014.” According to the report, Houston had gross domestic product growth of 12.3 percent between 2007 and 2012, and population growth of 11.5 percent during that time. Between 2007 and October 2013, Houston saw job growth of 9.2 percent. Forbes reports that a separate survey found Houston is one of the cities that have gained the most young college graduates since 2006, and the city is expected to add 140,000 new households by 2017 — the largest increase in the nation.</p>
		<p align="center"><img class="img-responsive" src="images/thewoodlands.jpg"/></p>
		<p><strong>17 Facts That Make Houston The Best City In America</strong></p>
		<ol>
			<li>
				<strong>It's got the jobs. Houston is the country's number one city for job creation. By a lot.</strong></li>
			<li>
				<strong>Its unemployment rate is way below the national level</strong></li>
			<li>
				<strong>A paycheck goes farther in Houston than any other major metropolitan area</strong></li>
		</ol>
		<p>
			Living well isn't just about high pay. It's about how much everything costs. You can't beat Houston here. When you adjust for cost of living, Houston has the highest pay in the country at $75,256, ahead of places like the San Jose area, which has high wages, but extremely high costs.</p>
		<ol>
			<li value="4">
				<strong>Housing is affordable: Houston didn't experience a housing bubble the way the rest of the country did.</strong></li>
			<li value="5">
				<strong>Its home to more Fortune 500 headquarters than anywhere in America except for New York</strong></li>
		</ol>
		<p>
			There are 22 Fortune 500 companies headquartered in Houston, fewer than New York's 45, but double Dallas or Atlanta, which tie for fourth with 10. Many are in the city's "energy corridor," the home of the oil and gas industry.&nbsp; They include Conoco Phillips, Marathon Oil, Sysco, Apache, Halliburton, and many more.</p>
		<ol>
			<li value="6">
				<strong>Houston is called Space City for a reason; it's home of the NASA Astronaut Corps</strong>.</li>
		</ol>
		<p>
			Houston is the home of the Lyndon B. Johnson Space Center. It's the home of America's astronaut corps, and the place where they — and many international astronauts — get trained to go to space. It's a significant research center and employer, as well as housing Mission Control for manned spaceflight expeditions.</p>
		<ol>
			<li value="7">
				<strong>The New York Times calls it 'one of the country's most exciting places to eat.</strong></li>
		</ol>
		<p>
			The New York Times recently gave glowing reviews to innovative Houston restaurants Oxheart and Underbelly. The paper's chief food critic, Pete Wells, wrote that Houston is becoming "one of the country’s most exciting places to eat." There's also an outpost of Uchi, one of America's most lauded sushi restaurants, and much more.</p>
		<ol>
			<li value="8">
				<strong>There's a spectacular range of ethnic cuisines, fantastic seafood, and great barbecue.</strong></li>
			<li value="9">
				<strong>Ignore the Astros. The Texans, Rockets, and Dynamo are all winners.</strong></li>
			<li value="10">
				<strong>It's one of the centers of America's booming oil and gas industry.</strong></li>
		</ol>
		<p>
			The oil and gas industry is booming in the United States. Not only is Houston the home of corporations like Conoco Phillips and Marathon, it's the center of the petrochemical industry, close to Texas oil fields, and close to Latin America. The energy sector provides an estimated 3.4% of all of the jobs in the area, and a large amount of the growth.</p>
		<ol>
			<li value="11">
				<strong>Massive international trade gives another big jobs boost to the rapidly growing city.</strong></li>
		</ol>
		<p>
			Houston's port is the largest in terms of international tonnage handled, and second overall. That means a great deal of international business and trade. That means even more jobs. It's also exceptionally business friendly, and is the only major US city without zoning laws. That port, its strength in the energy sector, and proximity to Latin America, saw more than 100 foreign-owned companies relocate, expand, or start new businesses in Houston between 2008 and 2010.</p>
		<ol>
			<li value="12">
				<strong>It hosts the world's largest concentration of health care organizations, with scientists working hard to beat cancer</strong></li>
		</ol>
		<p>
			The Texas Medical Center is the largest single employer in Houston, and the largest medical center in the world, with 21 hospitals, eight academic and research institutions, and 50 total related organizations, all not-for-profit. The complex is larger than downtown Dallas. Institutions include The University of Texas M.D. Anderson Cancer Center, one of the world's premier cancer treatment and research hospitals, which is spending billions in an aggressive push to cure five types of cancer.</p>
		<ol>
			<li value="13">
				<strong>The city is filled with world-class and unique museums and cultural landmarks, like the Rothko Chapel.</strong></li>
		</ol>
		<p>
			Houston's Museum of Fine Arts is among the largest museums in the United States, and one of the best collections of American decorative art and furniture in the house of a former trustee, along with 14 acres of former gardens.&nbsp; Mark Rothko spent three years creating the works of art that inhabit The Rothko Chapel, which he also helped design. There are 19 total museums in a 1.5 mile radius that make up the Houston Museum District.</p>
		<ol>
			<li value="14">
				<strong>Everybody loves parks. Houston's got more of them than any other top 10 metropolitan area.</strong></li>
		</ol>
		<p>
			Though it's known for its association with the oil industry, Houston has lots and lots of parks and green space 50,632 acres in total. That puts it third behind only San Diego and Dallas in acreage per capita. The city's been investing a large amount in building out this space, particularly building out the space surrounding Buffalo Bayou, Houston's main waterway.</p>
		<ol>
			<li value="15">
				<strong>The combination of The University of Houston and Rice University means there are a bunch of smart people around.</strong></li>
		</ol>
		<p style="margin-left:18.0pt;">
			It's not quite the college town that Boston is, but Houston's higher education is nothing to sniff at. The University of Houston has some 41,000 students, and was elevated to Tier One status as a research university by the Carnegie Foundation in 2011. Rice University is one of the country's best undergraduate schools, ranked 17th nationally, with particularly strong programs in applied sciences.</p>
		<ol>
			<li value="16">
				<strong>Houston recently passed New York to become the most ethnically and racially diverse city in the US.</strong></li>
		</ol>
		<p>
			According to census data, Houston is the most racially and ethnically diverse large metropolitan area in the United States. Some 400,000 foreign born residents moved to the city between 2000 and 2010.The Anglo population in Houston is 39.7, compared to 48.9 in New York, there are nearly as many Latinos as Anglos, and there's a huge and rapidly growing Asian population.</p>
		<ol>
			<li value="17">
				<strong>And finally, it's a great place for Southern hip hop!</strong></li>
		</ol>
		<p>
			Houston has produced artists like Chamillionaire, Mike Jones, Paul Wall, and Slim Thug.&nbsp; Beyoncé was born in Houston in 1981, and competed in the area's talent show circuit with her childhood friend Kelly Rowland and LaTavia Robertson as Girl's Tyme, which later became Destiny's Child.</p>
